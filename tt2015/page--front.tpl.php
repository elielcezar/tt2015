      <h2>Tribaltech</h2>
      
           <p>O Tribaltech Festival é um dos mais conceituados eventos de música, tecnologia, arte e comportamento do Brasil, realizado anualmente em Curitiba/PR, recebe público de todo o país, além da América Latina.</p>
          <p>O festival foi reconhecido ao longo dos anos em países como Alemanha, Reino Unido e Espanha, por suas inovações no mercado brasileiro que motivaram o crescimento criativo coletivo, através da apresentação ao público das mais diversas formas de arte vanguardista. Diferentes artes sob os mesmos pilares: música, vanguarda, tendência, tecnologia, alternatividade e liberdade de expressão.</p>
      
